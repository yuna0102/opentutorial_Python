#숫자형과 다르게 문자형은 '' 또는 ""를 써야한다.
print('HelloWorld')
print("HelloWorld")
#''를 표시하고 싶을 때는 "" 안에 ''를 넣어야 한다.
print("Hell'o' world")

#escape
#python에서 \의 기능은 해당 문자의 원래 기능을 해제시켜준다.
print("Hell'o' \"w\"orld")

